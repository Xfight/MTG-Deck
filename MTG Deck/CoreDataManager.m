#import "CoreDataManager.h"


@implementation CoreDataManager

@synthesize managedObjectContext=__managedObjectContext;
@synthesize managedObjectModel=__managedObjectModel;
@synthesize persistentStoreCoordinator=__persistentStoreCoordinator;

static CoreDataManager* _sharedCoreDataManager = nil;

+(CoreDataManager*)sharedCoreDataManager
{
	@synchronized([CoreDataManager class])
	{
		if (!_sharedCoreDataManager)
			[[self alloc] init];
        
		return _sharedCoreDataManager;
	}
    
	return nil;
}

+(id)alloc
{
	@synchronized([CoreDataManager class])
	{
		NSAssert(_sharedCoreDataManager == nil, @"Attempted to allocate a second instance of a singleton.");
		_sharedCoreDataManager = [super alloc];
		return _sharedCoreDataManager;
	}
    
	return nil;
}

-(id)init {
	self = [super init];
	if (self != nil) {
		// initialize stuff here
	}
    
	return self;
}





